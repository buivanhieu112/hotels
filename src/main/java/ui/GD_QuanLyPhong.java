    public String maPhongMoi() {
    	ArrayList<Phong> listPhong = DAO_Phong.getAllPhong();
    	if(listPhong.size()==0) {
    		return "PH001";
    	}
    	
    	String ma = listPhong.get(listPhong.size()-1).getMaPhong().substring(2);
    	
    	int i = Integer.parseInt(ma) + 1;
    	
    	if(i<10)
    		ma = "00" + i;
    	else if(i>=10 && i<=99)
    		ma = "0" + i;
    	else if(i>=100)
    		ma = i+"";
    	return "PH"+ma;
    }
    
