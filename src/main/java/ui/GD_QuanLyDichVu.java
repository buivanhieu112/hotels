    public void timDV(String ten) {
		ArrayList<DichVu> list = DAO_DichVu.timDV(ten);
		for (DichVu dv : list) {
			modelDSDV.addRow(new Object[] {
    				dv.getMaDV(),
    				dv.getTenDV(),
    				getTenLoaiDV(dv.getLoaiDichVu().getMaLoaiDichVu()),
    				getTenDonVi(dv.getDonVi().getMaDonVi()),
    				dv.getGiaDV(),
    				dv.getSoLuongTon()
    		});
		}
    }
    
