    public boolean themDV() throws ParseException {
    	String maDV, tenDV, loaiDV, donVi, maLDV, maDonVi;
    	Double giaDV;
    	int soLuongTon;
    	if(kiemTraDuLieuDV()) {
    		maDV = txtMaDV.getText();
        	tenDV = txtTenDV.getText();
//        	gia = txtGiaDV.getText();
        	giaDV= Double.parseDouble(txtGiaDV.getText());
//        	slt=txtSoLuongTon.getText();
        	soLuongTon = Integer.parseInt(txtSoLuongTon.getText());
        	loaiDV = cbxLDV.getSelectedItem()+"";
        	maLDV = layMaLDVTheoTen(loaiDV);
        	donVi = cbxDonVi.getSelectedItem()+"";
        	maDonVi = layMaDonViTheoTen(donVi);
        	
        	DichVu p = new DichVu(maDV, tenDV, giaDV, soLuongTon, new LoaiDichVu(maLDV), new DonVi(maDonVi) );
        	
        	if(!DAO_DichVu.themDV(p))
        		return false;
        	JOptionPane.showMessageDialog(rootPane, "Thêm dịch vụ thành công!");
        	modelDSDV.setRowCount(0);
        	DocDuLieuDatabaseVaoDSDV();
    	}
    	return true;
    }
    
    public String layMaLDVTheoTen(String tenLDV) {
    	ArrayList<LoaiDichVu> list = DAO_LoaiDichVu.getAllLoaiDV();
    	for (LoaiDichVu ldv : list) {
			if(tenLDV.equalsIgnoreCase(ldv.getTenLoaiDichVu()))
				return ldv.getMaLoaiDichVu();
		}
    	return "";
    }
    
    public String layMaDonViTheoTen(String tenDonVi) {
    	ArrayList<DonVi> list = DAO_DonVi.getAllDonVi();
    	for (DonVi dv : list) {
			if(tenDonVi.equalsIgnoreCase(dv.getTenDonVi()))
				return dv.getMaDonVi();
		}
    	return "";
    }
    
    public boolean kiemTraDuLieuDV() {
		String tenDV=txtTenDV.getText().trim();
//		String giaDV=txtGiaDV.getText().trim();
		int giaDV = 0;
		String gia = Integer.toString(giaDV);
		gia= txtGiaDV.getText().trim();
//		String soLuong = txtSoLuongTon.getText().trim();
		int soLuong = 0;
		String sl = Integer.toString(soLuong);
		sl = txtSoLuongTon.getText().trim();
		if (tenDV.trim().length() > 0) {
			if (!(tenDV.matches("[^.\\-\\/\\_\\+\\@\\!\\$\\^\\&\\*\\(\\)]+"))) {
				JOptionPane.showMessageDialog(this, "Tên dịch vụ không chứa ký tự đặc biệt");
				return false;
			}
		} else {
			JOptionPane.showMessageDialog(this, "Tên dịch vụ không được để trống");
			return false;
		}
		if (gia.trim().length()>0) {
			if (!(gia.matches("^[1-9]([0-9]{3,})"))) {
				JOptionPane.showMessageDialog(this, "Giá dịch vụ phải là số và lớn hơn bằng 1000");
				return false;
			}
		} else {
			JOptionPane.showMessageDialog(this, "Giá dịch vụ không được để trống");
			return false;
		}
		
		if (sl.trim().length()>0) {
			if (!(sl.matches("^[1-9]([0-9]{1,})"))) {
				JOptionPane.showMessageDialog(this, "Số lượng hiện có phải là số và lớn hơn 0");
				return false;
			}
		} else {
			JOptionPane.showMessageDialog(this, "Số lượng hiện có không được để trống");
			return false;
		}
		
		return true;
    }
    
