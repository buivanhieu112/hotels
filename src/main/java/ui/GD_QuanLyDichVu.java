    public void DocDuLieuVaoTextFiedDV()
    {
    	ArrayList<DichVu> list = DAO_DichVu.getAllDichVu();
		int i = tblDSDV.getSelectedRow();
		if(i!=-1) {
			DichVu dv = list.get(i);
			txtMaDV.setText(dv.getMaDV());
			txtTenDV.setText(dv.getTenDV());
			txtGiaDV.setText(String.valueOf(dv.getGiaDV()));
			txtSoLuongTon.setText(String.valueOf(dv.getSoLuongTon()));
			cbxLDV.setSelectedItem(getTenLoaiDV(dv.getLoaiDichVu().getMaLoaiDichVu()));
			cbxDonVi.setSelectedItem(getTenDonVi(dv.getDonVi().getMaDonVi()));
		}
    }
    
    
    
