    public void loadCBXSoPhong() {
    	String loaiPhong;
    	cbxTenPhong.removeAllItems();
    	loaiPhong = (String)cbxLoaiPhong.getSelectedItem();
    	try {
    		Database.getInstance().connect();
			Connection conn = Database.getConnection();
			String sql = "select * \n"
					+ "from Phong p join LoaiPhong lp on p.malp = lp.malp\n"
					+ " where lp.tenlp=N'" + loaiPhong + "' and tinhTrang=N'Còn Trống'";
			PreparedStatement pstm = conn.prepareStatement(sql);
			ResultSet rs= pstm.executeQuery();
			while(rs.next()) {
				cbxTenPhong.addItem(rs.getString("TenPhong"));
				txtGiaPhong.setText(getGiaLP((rs.getString("maLP"))));
			}
			
    	} catch(Exception e){
    		JOptionPane.showMessageDialog(null, e);
    	}
    }
    
