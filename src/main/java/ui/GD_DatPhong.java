    public String maHoaDonMoi() {
    	ArrayList<HoaDon> listHD = DAO_HoaDon.getAllHoaDon();
    	if(listHD.size()==0) {
    		return "HD001";
    	}
    	
    	String ma = listHD.get(listHD.size()-1).getMaHD().substring(2);
    	
    	int i = Integer.parseInt(ma) + 1;
    	
    	if(i<10)
    		ma = "00" + i;
    	else if(i>=10 && i<=99)
    		ma = "0" + i;
    	else if(i>=100)
    		ma = i+"";
    	return "HD"+ma;
    }
    public boolean kiemtradulieu() {
    	
		Timestamp s1 = new Timestamp(ngayDatPhong.getDate().getTime());
		Timestamp s2 = new Timestamp(ngayTraPhong.getDate().getTime());
		
		double s3 = s2.getTime() - s1.getTime();
		
		if(s3 <= 0) {
			JOptionPane.showMessageDialog(this, "Ngày trả phòng phải lớn hơn ngày đặt phòng");
			return false;
		}
		if(cbxTenPhong.getSelectedItem() == null) {
			JOptionPane.showMessageDialog(this, "Hết phòng");
			return false;
		}
		
    	return true;
    }
    
    
 
    
