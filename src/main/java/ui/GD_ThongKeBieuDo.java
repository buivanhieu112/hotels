    public boolean tinhNamNhuan(int nam) {
    	String haiSoCuoi = nam+"".substring(2);
    	if(haiSoCuoi.equalsIgnoreCase("00")) {
    		if(nam%400!=0)
    			return false;
    	}
    	else {
    		if(nam%4!=0)
    			return false;
    	}
    	return true;
    }
    
    public void thongKeDTThang(JPanel pnlThongKe, int thang, int nam) {
    	double tongThanhTien = 0;
    	double thanhTien = 0;
    	int soHD = 0;
    	ArrayList<String[]> listTKDT = null;
    	int ngayTrongThang;
    	if(thang==1||thang==3||thang==5||thang==7||thang==8||thang==10||thang==12)
    		ngayTrongThang = 31;
    	else if(thang==2) {
    		if(tinhNamNhuan(nam))
    			ngayTrongThang = 29;
    		else
    			ngayTrongThang = 28;
    	}
    	else
    		ngayTrongThang = 30;
    	DefaultCategoryDataset dataset = new DefaultCategoryDataset();
    	for(int i=1; i<=ngayTrongThang; i++) {
    		thanhTien = 0;
    		listTKDT = DAO_ThongKe.thongKeDTTheoNgay(i,thang, nam);
    		if(listTKDT!=null) {
    			for (int j=0; j<listTKDT.size(); j++) {
        			
        			thanhTien += Double.parseDouble(listTKDT.get(j)[1]);
        			soHD++;
				}
			}

    		tongThanhTien = tongThanhTien + thanhTien;
    		dataset.addValue(thanhTien, "Tổng thành tiền", i+"");
    	}
        JFreeChart barChart = ChartFactory.createBarChart(
                "Biểu đồ thống kê doanh thu tháng "+thang+"/"+nam+"".toUpperCase(),
                "Ngày","Tổng tiền",
                dataset, PlotOrientation.VERTICAL, false, true, true);
        
        CategoryPlot plot = barChart.getCategoryPlot();
        plot.setRangeGridlinePaint(Color.orange);
        ChartPanel chartPanel = new ChartPanel(barChart);
        chartPanel.setPreferredSize(new Dimension(pnlThongKe.getWidth(), 321));
        
        txtTongSoHD.setText(soHD+"");
        txtTongDT.setText(dinhDangTienTe(tongThanhTien));
        
        pnlThongKe.removeAll();
        pnlThongKe.setLayout(new CardLayout());
        pnlThongKe.add(chartPanel);
        pnlThongKe.validate();
        pnlThongKe.repaint();
    }
    
