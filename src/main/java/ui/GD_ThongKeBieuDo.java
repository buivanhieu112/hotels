    public void thongKeDichVu() {
    	String ngay, thang, nam;
    	int yyyy, mm, dd;
    	ngay = cbxNgayDV.getSelectedItem()+"";
    	thang = cbxThangDV.getSelectedItem()+"";
    	nam = cbxNamDV.getSelectedItem()+"";
    	
    	if(!nam.equalsIgnoreCase("    ")) {
    		yyyy = Integer.parseInt(nam);
    		if(!thang.equalsIgnoreCase("    ")) {
    			mm = Integer.parseInt(thang);
    			if(!ngay.equalsIgnoreCase("    ")) {
    				dd = Integer.parseInt(ngay);
    				thongKeNgayDV(pnlBieuDoDV, dd, mm, yyyy);
    				return;
    			}
    			thongKeThangDV(pnlBieuDoDV, mm, yyyy);
    			return;
    		}
    		thongKeNamDV(pnlBieuDoDV, yyyy);
    		return;
    	}
    	else
    		JOptionPane.showMessageDialog(rootPane, "Chưa chọn thời gian thống kê!", "Lỗi", JOptionPane.WARNING_MESSAGE);
    }
    
